import math

print(f"The floor and ceiling of 23.56 are: {math.floor(23.56)}, {math.ceil(23.56)}")

x = 10
y = -15
print(f"The value of x after copying the sign from y is: {math.copysign(x, y)}")

print(f"Absolute value of -96 and 56 are: {math.fabs(-96)}, {math.fabs(56)}")

print(f"The GCD of 24 and 56 is: {math.gcd(24, 56)}")